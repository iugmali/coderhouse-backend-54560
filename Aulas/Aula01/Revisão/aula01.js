// Tipos de Dados em JavaScript
let numero = 10;                // Número
let texto = "Olá, mundo!";      // String
let booleano = true;            // Booleano
let nulo = null;                // Nulo
let indefinido;                 // Indefinido
let objeto = {                  // Objeto
  nome: "Jenifer",
  idade: 33,
  profissao: "Engenheira"
};
let array = [1, 2, 3, 4, 5];    // Array

// Variáveis em JavaScript
var variavelGlobal = "Eu sou uma variável global";  // var
let variavelLocal = "Eu sou uma variável local";    // let
const semAlterar = 3.14;                                   // const

// Exibindo os valores no console
console.log("Número:", numero);
console.log("Texto:", texto);
console.log("Booleano:", booleano);
console.log("Nulo:", nulo);
console.log("Indefinido:", indefinido);
console.log("Objeto:", objeto);
console.log("Array:", array);

console.log("Variável Global:", variavelGlobal);
console.log("Variável Local:", variavelLocal);
console.log("Variável que o valor não pode alterar:", semAlterar);

// Tabela de Exemplos
console.log(" ");
