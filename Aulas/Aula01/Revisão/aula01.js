console.log("|--------------|---------------|-------------------------------------|");
console.log("| Número       |", numero, "       | Número inteiro                      |");
console.log("| String       |", texto, " | Sequência de caracteres             |");
console.log("| Booleano     |", booleano, "         | Valor lógico verdadeiro             |");
console.log("| Null         |", nulo, "          | Valor nulo                          |");
console.log("| Indefinido   |", indefinido, "     | Valor indefinido                   |");
console.log("| Objeto       |", objeto, " | Estrutura de dados complexa         |");
console.log("| Array        |", array, "       | Coleção ordenada de valores         |");
